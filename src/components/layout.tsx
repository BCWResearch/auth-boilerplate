import Card from "@mui/material/Card";
import CardContent from "@mui/material/CardContent";
import Container from "@mui/material/Container";
import Stack from "@mui/material/Stack";
import {
    Breakpoint,
    styled,
    SxProps,
    Theme,
    useTheme
} from "@mui/material/styles";

interface Props {
    centerLogo?: boolean;
    children: React.ReactNode;
    maxWidth?: false | Breakpoint;
    logo?: string | JSX.Element;
    sx?: {
        pageWrap?: SxProps<Theme>;
        card?: SxProps<Theme>;
    }
}

const Background = styled(`div`)(({ theme }) => ({
    position: `fixed`,
    top: `-20%`,
    left: 0,
    pointerEvents: `none`,
    width: `100%`,
    height: `120%`,
    background: `radial-gradient(circle, rgba(45,132,235,0.2) 0%, rgba(255,255,255,0.1) 100%)`,
    zIndex: -2,
    ...(theme.palette.mode === `light` && { backgroundColor: `rgba(247,248,250, 1)`, }),
    ...(theme.palette.mode === `dark` && { backgroundColor: `rgba(0,0,0, 1)`, }),
}));

export default function Layout (props: Props) {
    const {
        children,
        maxWidth,
        logo,
        sx
    } = props;

    const theme = useTheme();

    return (
        <Stack
            justifyContent={`space-around`}
            sx={{
                height: `100vh`,
                ...sx?.pageWrap
            }}
        >
            <Container maxWidth={maxWidth ?? false} sx={{ ...sx?.card }}>
                <Card variant="outlined">
                    <CardContent sx={{ p: `${theme.spacing(6, 5)} !important` }}>
                        <Stack spacing={2}>
                            { logo }
                            { children }
                        </Stack>
                    </CardContent>
                </Card>
            </Container>
            {/* <Background /> */}
        </Stack>
    );
}































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































